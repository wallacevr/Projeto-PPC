    public void setLblid(String lblid) {
        this.lblid.setText(lblid);
            EmpresasBO bo = new EmpresasBO();
        try {
             DefaultTableModel model = (DefaultTableModel) Jtbunidades.getModel();
             model.setRowCount(0);
                if(!lblid.isEmpty()){
                    Empresas empresa=bo.consultar(Integer.parseInt(lblid));
                    //lblid.setText(Integer.toString(empresa.getIdempresa()));
                    txtrazaosocial.setText(empresa.getRazao());
                    txtnomefantasia.setText(empresa.getNmfantasia());
                    txtlocalimagem.setText(empresa.getLogo());
                    chkativa.setSelected(empresa.isAtiva());
                    btnnova.setEnabled(false);
                    btnsalvar.setEnabled(false);
                    btnconsultar.setEnabled(false);
                    btnalterar.setEnabled(true);
                    btncancelar.setEnabled(true);
                    btnlistar.setEnabled(false);
                    Component[] componentes =  JpanelEmpresas.getComponents(); // altere para o nome da variavel do seu painel
                    for (Component componente : componentes) {
                        componente.setEnabled(true);
                    }
                    UnidadesBO bouni=new UnidadesBO();
                    try{
                        List<Unidades> lista=bouni.listar(Integer.parseInt(lblid));
                        for(Unidades unidade:lista){
                            
                            //Object[] linha = //alguma linha
                            
                            //model.addRow(linha);
                            model.addRow(new Object[]{unidade.getCNPJ(),unidade.getNomeUnidade(),convertbol(unidade.isAtiva()),convertbol(unidade.isMatriz())});
                        }
                    }catch(NegocioException ex){

                    } catch (DadosException ex) {
                        Logger.getLogger(JFIEmpresas.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else {
                }
        }    
                catch (NegocioException ex) {
                   // Logger.getLogger(JFIEmpresa.class.getName()).log(Level.SEVERE, null, ex);
                }
    }

