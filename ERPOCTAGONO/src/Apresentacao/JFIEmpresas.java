    private void btnsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsalvarActionPerformed
        // TODO add your handling code here:
        Empresas empresa= new Empresas();
        empresa.setRazao(txtrazaosocial.getText());
        empresa.setNmfantasia(txtnomefantasia.getText());
        empresa.setLogo(txtlocalimagem.getText());
        empresa.setAtiva(chkativa.isSelected());
        EmpresasBO bo = new EmpresasBO();
        try{
            if(empresa.getIdempresa()==0){
                bo.inserir(empresa);
                btnnova.setEnabled(true);
                btnsalvar.setEnabled(false);
                btnconsultar.setEnabled(true);
                btnalterar.setEnabled(false);
                btncancelar.setEnabled(false);
                txtrazaosocial.setText("");
                txtnomefantasia.setText("");
                txtlocalimagem.setText("");
                JpanelEmpresas.setEnabled(true);
                Jtbunidades.setEnabled(false);
                btnlistar.setEnabled(true);
                Component[] componentes =  JpanelEmpresas.getComponents(); // altere para o nome da variavel do seu painel
                for (Component componente : componentes) {
                    componente.setEnabled(false);
                }
                DefaultTableModel model = (DefaultTableModel) Jtbunidades.getModel();
                model.setRowCount(0);
            }

        }catch(NegocioException e){
            JOptionPane.showMessageDialog(null,e.getMessage() ,"Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnsalvarActionPerformed

    private void btnconsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnconsultarActionPerformed
        // TODO add your handling code here:
       
            
        int idconsulta = Integer.parseInt(JOptionPane.showInputDialog("Digite o ID da empresa"));
        EmpresasBO bo = new EmpresasBO();
        try {
             DefaultTableModel model = (DefaultTableModel) Jtbunidades.getModel();
             model.setRowCount(0);
            Empresas empresa=bo.consultar(idconsulta);
            lblid.setText(Integer.toString(empresa.getIdempresa()));
            txtrazaosocial.setText(empresa.getRazao());
            txtnomefantasia.setText(empresa.getNmfantasia());
            txtlocalimagem.setText(empresa.getLogo());
            chkativa.setSelected(empresa.isAtiva());
            btnnova.setEnabled(false);
            btnsalvar.setEnabled(false);
            btnconsultar.setEnabled(false);
            btnalterar.setEnabled(true);
            btncancelar.setEnabled(true);
            btnlistar.setEnabled(false);
            Component[] componentes =  JpanelEmpresas.getComponents(); // altere para o nome da variavel do seu painel
            for (Component componente : componentes) {
                componente.setEnabled(true);
            }
            Jtbunidades.setEnabled(false);
            UnidadesBO bouni=new UnidadesBO();
            try{
                List<Unidades> lista=bouni.listar(Integer.parseInt(lblid.getText()));
                for(Unidades unidade:lista){
                   
                //Object[] linha = //alguma linha
 
                //model.addRow(linha);
                    model.addRow(new Object[]{unidade.getCNPJ(),unidade.getNomeUnidade(),convertbol(unidade.isAtiva()),convertbol(unidade.isMatriz())});
                }
            }catch(NegocioException ex){
            
            } catch (DadosException ex) {
                Logger.getLogger(JFIEmpresas.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (NegocioException ex) {
           // Logger.getLogger(JFIEmpresa.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnconsultarActionPerformed

